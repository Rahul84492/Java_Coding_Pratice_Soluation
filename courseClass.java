class Course{
    static int maxCapacity;
    String courseName;
    int enrollStudents;
    String[] enrollStudent = new String[maxCapacity];

    static void setMaxCapcity(int maxCapacity){
       Course.maxCapacity=maxCapacity;
    }
    void enrollStudent(String studentNameString){
        enrollStudent[enrollStudents]=studentName;
        enrollStudents++;
    }
    void unenrollStudent(String studentNameString){
        enrollStudent[enrollStudents]=studentName;
        enrollStudents++;
    }

}

